package entity;

public class Quiz {

    private int id;
    private String name;
    private String level;
    private int  duration;
    private int passRate;
    private String description;
    private int testTypeId;
    private int quizSettingId;
    private int subjectId;
    private int status;
    private String quizFormat;

    public int getTestTypeId() {
        return testTypeId;
    }

    public void setTestTypeId(int testTypeId) {
        this.testTypeId = testTypeId;
    }

    public int getPassRate() {
        return passRate;
    }

    public void setPassRate(int passRate) {
        this.passRate = passRate;
    }

    public int getDuration() {
        return duration;
    }

    public void setDuration(int duration) {
        this.duration = duration;
    }

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public int getSubjectId() {
        return subjectId;
    }

    public void setSubjectId(int subjectId) {
        this.subjectId = subjectId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getQuizSettingId() {
        return quizSettingId;
    }

    public void setQuizSettingId(int quizSettingId) {
        this.quizSettingId = quizSettingId;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getQuizFormat() {
        return quizFormat;
    }

    public void setQuizFormat(String quizFormat) {
        this.quizFormat = quizFormat;
    }
}
